

class Cars:

    def __init__(self, brend, color, power, model):
        self.brend = brend
        self.color = color
        self.power = power
        self.model = model

    def get_color(self):
        return self.color

    def get_brend(self):
        return self.brend

    def get_power(self):
        return self.power

    def get_model(self):
        return self.model

